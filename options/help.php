<div style="float: left">
    <h2>
        Simple Customize field overview
    </h2>

    <p>
        <strong>
            Name / Label
        </strong>

        <br />

        The label is used to describe what you wish to change, this is also used as an identified and should be unique.
    </p>

    <p>
        <strong>
            Category
        </strong>

        <br />

        The category determines which tab the option is listed under, this can for example be used to list all header, widget and footer related customizations separately.
    </p>

    <p>
        <strong>
            CSS selector
        </strong>

        <br />

        The class and id path of the element you wish to easily make adjustments too.<br />
        It may be beneficial to have some knowledge of CSS if you wish to make modifications here manually, the plugin will be <span style="text-decoration: underline;">extremely</span> specific on what it chooses.
    </p>

    <p>
        <strong>
            What to customize
        </strong>

        <br />

        The attribute that you wish to change, this will be automatically populated with available options when an element is selected on your page, but can also be manually set if it's a newer attribute not known to all browsers yet.
    </p>

    <p>
        <strong>
            Default value
        </strong>

        <br />

        The default value will be automatically populated when you pick something to customize, and varies depending on the selected attribute.
    </p>

    <p>
        <strong>
            Find element
        </strong>

        <br />

        After clicking the Find element button, click any element in the preview window to auto-populate the fields.
    </p>
</div>
